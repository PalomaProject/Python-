"""Desafio 6: crie um programa que leia um numero e mostre seu dobro, triplo e a raiz quadrada"""

numero = int(input('digite um numero'))

dobro = numero * 2
triplo = numero * 3
raiz = numero ** (1/2)

print('o dobro do numero digitado é {}, o triplo é {} e a raiz quadrada é {:.3f}' .format(dobro,triplo,raiz))



