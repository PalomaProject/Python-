""" desafio 5: faça um programa que leia um numero inteiro e mostre na tela o seu antecessor e o seu
sucessor"""

numero = (int(input('digite um numero')))
somar = numero + 1
diminuir = numero - 1
print('analisando o valor {}, seu antecessor é {} o seu sucessor é {}' .format(numero,diminuir,somar))
